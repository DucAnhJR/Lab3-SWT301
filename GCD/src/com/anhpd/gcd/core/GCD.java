package com.anhpd.gcd.core;

/**
 *
 * @author ADMIN
 */
public class GCD {
    public static int findGCD(int a, int b) {
        while (b != 0) {
            int temp = b;
            b = a % b;
            a = temp;
        }
        return a;
    }
}
